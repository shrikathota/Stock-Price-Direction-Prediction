import yfinance as yf
import pandas as pd
import numpy as np
import matplotlib.pyplot as plt
import ta

from sklearn.preprocessing import MinMaxScaler
from sklearn.model_selection import train_test_split
from sklearn.metrics import accuracy_score, confusion_matrix, classification_report
from sklearn.utils.class_weight import compute_class_weight
from imblearn.over_sampling import RandomOverSampler

from tensorflow.keras.models import Sequential
from tensorflow.keras.layers import LSTM, Dense, Dropout
from tensorflow.keras.callbacks import EarlyStopping

# 1. Download Data
df = yf.download(tickers="AAPL", interval="5m", period="60d")
df.dropna(inplace=True)

# 2. Feature Engineering
close = df['Close'].squeeze()
high = df['High'].squeeze()
low = df['Low'].squeeze()
volume = df['Volume'].squeeze()

# Momentum
df['RSI'] = ta.momentum.RSIIndicator(close).rsi()
df['Stoch'] = ta.momentum.StochasticOscillator(high, low, close).stoch()

# Trend
df['MACD'] = ta.trend.MACD(close).macd()
df['EMA'] = ta.trend.EMAIndicator(close).ema_indicator()

# Volatility
bb = ta.volatility.BollingerBands(close)
df['BB_high'] = bb.bollinger_hband()
df['BB_low'] = bb.bollinger_lband()

# Volume
df['Volume_Change'] = volume.pct_change()

df.dropna(inplace=True)

# 3. Create Target Variable
# Threshold to remove noise (e.g., return > 0.1%)
df['Return'] = df['Close'].pct_change().shift(-1)
df['Target'] = np.where(df['Return'] > 0.001, 1, 0)

df.dropna(inplace=True)

# 4. Select and Clean Features
features = [
    'Close', 'RSI', 'Stoch', 'MACD', 'EMA',
    'BB_high', 'BB_low', 'Volume_Change'
]

X = df[features].copy()
y = df['Target'].copy()

# Replace infinity with NaN and drop
X = X.replace([np.inf, -np.inf], np.nan)
X = X.dropna()
y = y.loc[X.index]

# 5. Normalize Data
scaler = MinMaxScaler()
X_scaled = scaler.fit_transform(X)

# 6. Create Sequences for LSTM
def create_sequences(data, target, seq_len=50):
    X_seq, y_seq = [], []
    for i in range(len(data) - seq_len):
        X_seq.append(data[i:i+seq_len])
        y_seq.append(target[i+seq_len])
    return np.array(X_seq), np.array(y_seq)

X_seq, y_seq = create_sequences(X_scaled, y.values, seq_len=50)

# Split Data (Stratified because of class imbalance)
X_train, X_test, y_train, y_test = train_test_split(
    X_seq, y_seq, test_size=0.2, shuffle=True, stratify=y_seq, random_state=42
)

# 7. Apply Oversampling
# Reshape 3D to 2D for RandomOverSampler
original_shape = X_train.shape
X_train_reshaped = X_train.reshape(original_shape[0], -1)

os = RandomOverSampler(random_state=42)
X_train_resampled_2d, y_train_resampled = os.fit_resample(X_train_reshaped, y_train)

# Reshape back to 3D for LSTM
X_train_resampled = X_train_resampled_2d.reshape(-1, original_shape[1], original_shape[2])

print(f"Original training shape: {original_shape}")
print(f"Resampled training shape: {X_train_resampled.shape}")
print(f"Resampled target distribution: {np.bincount(y_train_resampled)}")

# 8. Compute Class Weights
classes = np.unique(y_train_resampled)
weights = compute_class_weight(class_weight='balanced', classes=classes, y=y_train_resampled)
class_weights = dict(zip(classes, weights))

# 9. Build LSTM Model
model = Sequential([
    LSTM(100, return_sequences=True, input_shape=(X_train.shape[1], X_train.shape[2])),
    Dropout(0.4),
    LSTM(50),
    Dropout(0.4),
    Dense(25, activation='relu'),
    Dense(1, activation='sigmoid')
])

model.compile(loss='binary_crossentropy', optimizer='adam', metrics=['accuracy'])
model.summary()

# 10. Training with Early Stopping
early_stop = EarlyStopping(
    monitor='val_loss',
    patience=5,
    restore_best_weights=True
)

history = model.fit(
    X_train_resampled, y_train_resampled,
    epochs=30,
    batch_size=64,
    validation_data=(X_test, y_test),
    callbacks=[early_stop],
    class_weight=class_weights
)

# 11. Evaluation
pred = (model.predict(X_test) > 0.5).astype(int)

print("\n--- Model Evaluation ---")
print("Accuracy:", accuracy_score(y_test, pred))
print("\nConfusion Matrix:\n", confusion_matrix(y_test, pred))
print("\nClassification Report:\n", classification_report(y_test, pred))

# 12. Plotting
plt.figure(figsize=(12, 5))

plt.subplot(1, 2, 1)
plt.plot(history.history['accuracy'], label='Train Accuracy')
plt.plot(history.history['val_accuracy'], label='Validation Accuracy')
plt.title("Accuracy Plot")
plt.legend()

plt.subplot(1, 2, 2)
plt.plot(history.history['loss'], label='Train Loss')
plt.plot(history.history['val_loss'], label='Validation Loss')
plt.title("Loss Plot")
plt.legend()

plt.tight_layout()
plt.show()
